

document.getElementById('uClick').addEventListener('click', () => {
    // alert('Button was pressed!');
    var x = document.getElementById("udesc");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
})

